        /// <summary>
        /// ボタン押下時のショートカット動作
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void FormKeyDown(object sender, KeyEventArgs e)
        {
            try
            {
                switch (e.KeyCode)
                {
                    case Keys.F9:
                        // 色名ラベルのプリント
                        BtnColorNamePrint.PerformClick();
                        break;
                    case Keys.F6:
                        // キャンセル
                        BtnCancel.PerformClick();
                        break;
                    case Keys.F7:
                        // 閉じる
                        BtnClose.PerformClick();
                        break;
                }
            }
            catch (Exception ex)
            {
                PutLog(ex);
            }
        }
